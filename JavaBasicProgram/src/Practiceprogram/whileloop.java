package Practiceprogram;

public class whileloop {

	public static void main(String[] args) {
		//printing number 4-0
int i=4;
while(i>0) {
	System.out.println(""+i);
i--;
}
//System.out.print("+++++++++++++++++++++++++++++++++");

//printing alphabets
char a='A';
while(a<='Z') {
	System.out.println(  " "  +a);
a++;
}
//System.out.println("+++++++++++++++++++++++++++++++++");
//reverse alphabates
char b='Z';
while(b>='A') {
	System.out.print( " "  +b);
	b--;
}
	}

}
