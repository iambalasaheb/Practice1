package JavaBasic;

public class Ifelseifcodition2 {

	public static void main(String[] args) {
		int number = 0;
		if (number > 0) {
			System.out.println("POSITIVE number");
		} 
		else if (number < 0) {
			System.out.println("NEGATIVE number");
		} 
		else {
			System.out.println("ZERO");
		}
	
	}

}
